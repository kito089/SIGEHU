-- -----------------------------------------------------------------------------
-- SP_ABRIR_GARANTIA
-- Abre un reporte de garantía y actualiza el estado de la obra a "Garantia" (6).
-- Se ejecuta en una sola transacción: si falla alguna parte, rollback automático.
-- -----------------------------------------------------------------------------
CREATE OR ALTER PROCEDURE SP_ABRIR_GARANTIA (
    pIdObra          INTEGER,
    pIdTrabajador    INTEGER,
    pDescripcion     BLOB SUB_TYPE TEXT
)
RETURNS (
    oIdGarantia INTEGER
)
AS
BEGIN
    -- Cambiar estado de la obra a "Garantia"
    UPDATE Obras
    SET EstadosObra_idEstadoObra = 6   -- Garantia
    WHERE idObra = :pIdObra
      AND EstadosObra_idEstadoObra = 5; -- Solo desde "Instalado"

    IF (ROW_COUNT = 0) THEN
        EXCEPTION;  -- Lanza excepción genérica; el backend captura y devuelve error

    INSERT INTO Garantias
        (Obras_idObra, Descripcion, Trabajadores_idTrabajador, EstadosGarantia_idEstadoGarantia)
    VALUES
        (:pIdObra, :pDescripcion, :pIdTrabajador, 1)  -- 1 = Reportada
    RETURNING idGarantia INTO :oIdGarantia;

    SUSPEND;
END^

-- -----------------------------------------------------------------------------
-- SP_CERRAR_GARANTIA
-- Marca la garantía como resuelta y opcionalmente cierra la obra.
-- -----------------------------------------------------------------------------
CREATE OR ALTER PROCEDURE SP_CERRAR_GARANTIA (
    pIdGarantia          INTEGER,
    pDescripcionResolucion BLOB SUB_TYPE TEXT,
    pCerrarObra          SMALLINT  -- 1 = pasar obra a Finalizado
)
AS
    DECLARE VARIABLE vIdObra INTEGER;
BEGIN
    SELECT Obras_idObra FROM Garantias WHERE idGarantia = :pIdGarantia
    INTO :vIdObra;

    UPDATE Garantias
    SET EstadosGarantia_idEstadoGarantia = 3,     -- Resuelta
        DescripcionResolucion             = :pDescripcionResolucion
    WHERE idGarantia = :pIdGarantia;

    IF (pCerrarObra = 1) THEN
    BEGIN
        -- Solo cierra si TODAS las garantías de la obra están resueltas
        UPDATE Obras
        SET EstadosObra_idEstadoObra = 7   -- Finalizado
        WHERE idObra = :vIdObra
          AND NOT EXISTS (
              SELECT 1 FROM Garantias
              WHERE Obras_idObra = :vIdObra
                AND EstadosGarantia_idEstadoGarantia <> 3  -- Distintas de Resuelta
                AND Activo = TRUE
          );
    END
END^

