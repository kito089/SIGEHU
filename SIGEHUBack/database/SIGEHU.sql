-- VW_BUSQUEDA_GLOBAL
-- Vista para el buscador global (RF-29). El backend hace LIKE contra esta vista.
-- Unifica clientes, obras y proveedores en una sola tabla de resultados.
-- -----------------------------------------------------------------------------
CREATE OR ALTER VIEW VW_BUSQUEDA_GLOBAL AS
-- Clientes
SELECT
    'cliente'           AS TipoRegistro,
    c.idCliente         AS IdRegistro,
    c.NombreCompleto    AS TextoPrimario,
    c.Telefono          AS TextoSecundario,
    NULL                AS TextoTerciario
FROM Clientes c
WHERE c.Activo = TRUE

UNION ALL

-- Obras
SELECT
    'obra'              AS TipoRegistro,
    o.idObra            AS IdRegistro,
    o.Nombre            AS TextoPrimario,
    c.NombreCompleto    AS TextoSecundario,
    e.Nombre            AS TextoTerciario
FROM Obras o
JOIN Clientes c    ON c.idCliente  = o.Clientes_idCliente
JOIN EstadosObra e ON e.idEstadoObra = o.EstadosObra_idEstadoObra
WHERE o.Activo = TRUE

UNION ALL

-- Proveedores
SELECT
    'proveedor'         AS TipoRegistro,
    p.idProveedor       AS IdRegistro,
    p.Nombre            AS TextoPrimario,
    p.Telefono          AS TextoSecundario,
    p.Correo            AS TextoTerciario
FROM Proveedores p
WHERE p.Activo = TRUE;