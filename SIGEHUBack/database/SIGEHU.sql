-- SP_OTORGAR_PERMISOS_OBRA
-- Otorga múltiples permisos a un trabajador en una obra de una sola llamada.
-- pListaPermisos: lista de idCampoPermiso separados por coma, ej: '1,2,5'
-- NOTA: Firebird 5 no tiene split nativo; se usa una alternativa con EXECUTE BLOCK
-- o se itera desde Node. La versión recomendada es iterar desde Node llamando
-- SP_OTORGAR_PERMISO_OBRA por cada campo. Este SP maneja uno a la vez.
-- -----------------------------------------------------------------------------
CREATE OR ALTER PROCEDURE SP_OTORGAR_PERMISO_OBRA (
    pIdObra          INTEGER,
    pIdTrabajador    INTEGER,
    pIdCampoPermiso  INTEGER
)
AS
BEGIN
    -- UPDATE OR INSERT para idempotencia (no falla si ya existe)
    UPDATE OR INSERT INTO PermisosGranularesObras
        (CamposPermiso_idCampoPermiso, Obras_idObra, Trabajadores_idTrabajador)
    VALUES
        (:pIdCampoPermiso, :pIdObra, :pIdTrabajador)
    MATCHING (CamposPermiso_idCampoPermiso, Obras_idObra, Trabajadores_idTrabajador);
END^

